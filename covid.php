<h3 >Fund Raising for supporting current students due to corona issue
  </h3>
<p>Humanity remains at its best when it can be applied in its proper place in the right hour. We have already been aware of this fact that our earth including our beloved country is suffering today with this infectious disease "Coronavirus". It is the highest time to work together and fight against this Pandemic "COVID-19".

So, JU CSE Alumni Association, Jahangirnagar University along with the help of current students, wants to support the juniors of our department during this pandemic by assisting economically those who are facing financial issues in their family. We want to raise a fund to successfully complete this task.

We, JU CSE Alumni Association, hope that all members of the family will be able to cope with this danger and lead a healthy normal life during this epidemic. We hope everyone will extend their support to the side as much as possible.

 

There is a very important concern that we must share with you. We cannot disclose any information about those financially weak students (their name/id/district/donation - nothing.).</p>
