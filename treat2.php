<p>
আমাদের ডিপার্টমেন্টের আন্ডারগ্রাজুয়েট প্রোগ্রামের  ৭ ব্যাচের শিক্ষার্থী তানিয়ার বাবা ক্যান্সারে আক্রান্ত। তিনি বেশ দিন ধরে গলার সমস্যায় ভুগছিলেন। প্রথমে গলায় টিউমার ধরা পড়ে।কিন্তু অপারেশনের পর তার ক্যান্সারের বিষয়টা নিশ্চিত হয়।<br>

তারপর ডাক্তার তাকে মহাখালী ক্যান্সার হাসপাতালে রেফার করেন। এখন এটা ২-গ্রেডে আছে। ডাক্তার বলেছেন, '"তার দ্রুত চিকিৎসার ব্যবস্থা করলে সুস্থ হয়ে উঠা স্বাভাবিক। প্রাথমিকভাবে ক্যান্সারের চিকিৎসা করতে প্রায় দেড় লক্ষ টাকা প্রয়োজন।"<br>

উল্লেখ যে তানিয়ার বাবাই ছিল তাদের পরিবারের একমাত্র উপার্জনক্রম ব্যক্তি। তার বাবার অসুস্থতার কারণে পরিবারটির আর্থিক সংকট ভয়াবহ রূপ ধারণ করেছে।<br>

আপনারা জানেন যে, পাবলিক বিশ্ববিদ্যালয়ের অধিকাংশ ছেলে-মেয়ে মধ্যবিত্ত অথবা নিম্নমধ্যবিত্ত পরিবার থেকে আগত।তাদের পরিবারের আর্থিক সংকট টা লেগেই থাকে।সবচেয়ে মর্মস্পর্শী বিষয় এই যে, আর্থিক সংকটের কারণে তার চিকিৎসা শুরু করা সম্ভব হচ্ছে না। আপাতত উনি উনার গ্রামের বাড়িতেই আছেন।<br>

আমাদের একটু সাহায্যে আমাদের এই বোনটি তার বাবাকে বাঁচানোর স্বপ্ন দেখতে পারে।আসুন একজন বাবাকে বাঁচাতে এগিয়ে আসি। আমাদের একটু আর্থিক সাহায্য একটা পরিবারের স্বপ্ন বাঁচিয়ে রাখতে পারে।
 
